// ============================================
// STAGGERED ANIMATION FOR GRIDS
// ============================================
const animateGrid = (gridSelector) => {
    const grid = document.querySelector(gridSelector);
    if (!grid) return;
    
    const items = grid.children;
    Array.from(items).forEach((item, index) => {
        setTimeout(() => {
            item.style.opacity = '1';
            item.style.transform = 'translateY(0)';
        }, index * 100);
    });
};

// Observe grids
const gridObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const gridClass = entry.target.className.split(' ')[0];
            animateGrid(`.${gridClass}`);
            gridObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.1 });

const grids = document.querySelectorAll('.theme-grid, .speakers-grid, .focus-grid');
grids.forEach(grid => gridObserver.observe(grid));

